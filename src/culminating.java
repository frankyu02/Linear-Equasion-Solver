    public double calculatex(){
       double x;
       x=(double)(c1*yint2-yint1*c2)/(xint1*yint2-yint1*xint2);//solves for the x intercept using Cramer's law
       return x;//returns the x value calculated 
    }
    public double calculatey(){
        double y;
        y=(double)(xint1*c2-c1*xint2)/(xint1*yint2-yint1*xint2);
        return y;//returns the x value calculated
    }

