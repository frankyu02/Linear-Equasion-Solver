    private void btnstartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnstartActionPerformed
    btnsubmit.setEnabled(true);//enables the submit button upon press
    txtquestion.setText("");//clears all the boxes on the interface except for the questions
    txtoutput.setText("");
    txtinputx.setText("");
    txtinputy.setText("");
    do{
       xint1=(int)(Math.random()*100+1);//these 4 lines provide a randomly generated number for the linear function
       xint2=(int)(Math.random()*100+1);
       yint1=(int)(Math.random()*100+1);
       yint2=(int)(Math.random()*100+1);
       c1=(int)(Math.random()*100+1);
       c2=(int)(Math.random()*100+1);
    }while((xint1*yint2)-(xint2*yint1)==0);//Cramer's law states that ths function is solvable as long as the difference between the product of x variable and y variables of opposite functions do not equal 0
   txtquestion.append("1) "+xint1+"x+"+yint1+"y="+c1+"\n");//outputs the two functions generated
   txtquestion.append("2) "+xint2+"x+"+yint2+"y="+c2);
   xintercept=(Math.round(calculatex()*100.0))/100.0;//takes calculations and rounds it to 2 decimal places
   yintercept=(Math.round(calculatey()*100.0))/100.0;
   
    
    }//GEN-LAST:event_btnstartActionPerformed

    private void btnsubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsubmitActionPerformed
       double x,y;
       txtoutput.setText("");//clears the output box each time submit is pressed
       x=Double.parseDouble(txtinputx.getText());//takes the user input for x value
       y=Double.parseDouble(txtinputy.getText());//takes the user input for y value
       if(x==xintercept&&y==yintercept){//if the answer inputted match the answer outputted, it send a statement stating correct
           txtoutput.setText("Correct!");
       }
       else{//if the answers are wrong, it states that it is incorrect and provides the right answer
           txtoutput.append("Incorrect Answer\n");
           txtoutput.append("your Answer: x="+x+" y="+y+"\n");
           txtoutput.append("Correct Answer: x="+xintercept+" y="+yintercept);
           
       }
       
    }//GEN-LAST:event_btnsubmitActionPerformed

